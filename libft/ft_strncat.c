#include "libft.h"

char		*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*ret;
	size_t	i;

	ret = s1;
	i = 0;
	while (*s1 != '\0')
		s1++;
	while (*s2 != '\0' && i < n)
	{
		*s1 = *s2;
		s1++;
		s2++;
		i++;
	}
	*s1 = '\0';
	return (ret);
}

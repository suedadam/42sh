#include "libft.h"

char		*ft_strdup(const char *s1)
{
	char	*str;
	size_t	len;

	len = ft_strlen(s1);
	if ((str = (char *)malloc(len * sizeof(*str) + 1)) == NULL)
		return (NULL);
	str = (char *)ft_memcpy(str, s1, len);
	str[len] = '\0';
	return (str);
}
